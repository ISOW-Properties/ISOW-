function filterProperties() {

    let filtered = properties;

    const city =
        document.getElementById("cityFilter").value;

    const primeLocation =
        document.getElementById("primeLocationFilter").value;

    const price =
        document.getElementById("priceFilter").value;

    // City Filter
    if (city) {
        filtered = filtered.filter(
            p => p.city === city
        );
    }

    // Prime Location Filter
    if (primeLocation) {
        filtered = filtered.filter(
            p => p.primeLocation === primeLocation
        );
    }

    // Price Filter
  // Price Filter
if (price) {

    if (price === "200+") {

        filtered = filtered.filter(
            p => p.totalPrice > 200
        );

    } else {

        filtered = filtered.filter(
            p => p.totalPrice <= Number(price)
        );

    }

}

    displayProperties(filtered);
}

function searchProperty() {

    const search =
        document.getElementById("searchInput")
        .value
        .toLowerCase();

    const filtered =
        properties.filter(p =>

            p.city.toLowerCase().includes(search) ||

            p.primeLocation.toLowerCase().includes(search) ||

            p.title.toLowerCase().includes(search)

        );

    displayProperties(filtered);

}

function loadLocations() {

    const datalist =
        document.getElementById("locationsList");

    datalist.innerHTML = "";

    const locations = [
        ...new Set(
            properties.flatMap(p => [
                p.city,
                p.primeLocation
            ])
        )
    ];

    locations.sort();

    locations.forEach(location => {

        datalist.innerHTML += `
            <option value="${location}">
        `;

    });

}
