function loadCityDropdown() {

const cityDropdown =
document.getElementById("cityFilter");

cityDropdown.innerHTML =
'<option value="">All Cities</option>';

const cities =
[...new Set(properties.map(p => p.city))];

cities.sort();

cities.forEach(city => {

cityDropdown.innerHTML +=
`<option value="${city}">
${city}
</option>`;

});

}

function updatePrimeLocations() {

    const city =
        document.getElementById("cityFilter").value;

    const primeDropdown =
        document.getElementById("primeLocationFilter");

    primeDropdown.innerHTML =
        '<option value="">All Prime Locations</option>';

    let locations = [];

    if (city) {

        locations = [
            ...new Set(
                properties
                    .filter(p => p.city === city)
                    .map(p => p.primeLocation)
            )
        ];

    } else {

        locations = [
            ...new Set(
                properties.map(
                    p => p.primeLocation
                )
            )
        ];

    }

    locations.sort();

    locations.forEach(location => {

        primeDropdown.innerHTML += `
            <option value="${location}">
                ${location}
            </option>
        `;

    });

}
